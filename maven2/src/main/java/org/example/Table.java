package org.example;

public class Table {
    public static void main(String[] args) {
        int tableno = 5;
        int increament = 0;

        for (increament = 10; increament > 0; increament -=2 ){
            System.out.println(tableno+" * " + (increament) + " = "+ tableno* (increament));
        }
    }
}









